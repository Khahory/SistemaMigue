    private void insertarDatos() {
       // if (probarCamposVacios() == true) {
            //pasarTFaVariables();
            try {
                PreparedStatement ppt = cn.prepareStatement("INSERT INTO Inventario(Centro, Zona, CPU, Mouse, Monitor, Teclado, UPS, Impresora, Laptop, Bulto, Cargador, ModemUSB, Internet, Serial) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
                //ppt.setString(1, tf_idproducto_RC.getText());
                ppt.setString(1, tf_centro.getText());
                ppt.setInt(2, Integer.parseInt(tf_zona.getText()));
                ppt.setInt(3, Integer.parseInt(tf_cpu.getText()));
                ppt.setInt(4, Integer.parseInt(tf_mouse.getText()));
                ppt.setInt(5, Integer.parseInt(tf_monitor.getText()));
                ppt.setInt(6, Integer.parseInt(tf_teclado.getText()));
                ppt.setInt(7, Integer.parseInt(tf_ups.getText()));
                ppt.setInt(8, Integer.parseInt(tf_impresora.getText()));
                ppt.setInt(9, Integer.parseInt(tf_laptop.getText()));
                ppt.setInt(10, Integer.parseInt(tf_bulto.getText()));
                ppt.setInt(11, Integer.parseInt(tf_cargador.getText()));
                ppt.setInt(12, Integer.parseInt(tf_modem.getText()));
                ppt.setString(13, tf_internet.getText());
                ppt.setString(14, tf_serial.getText());
     
                ppt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Datos guardados correctamente");
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Hubo un error al insertar los datos", "Problema con los datos", JOptionPane.QUESTION_MESSAGE);
                Logger.getLogger(MenuMain.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
    
